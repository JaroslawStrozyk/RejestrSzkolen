    private void GenerujKod(){//String ustawionyRok){
        try{
            String s;
            int row = jTable1.getSelectedRow();
            int licznik = MAIN.wpisySzkolenie;
            String rok = CONF.aktualnyRok;// ustawionyRok;//CONF.aktualnyRok;
            licznik = licznik + 1;
            if (licznik<10){
                s = "0"+Integer.toString(licznik);
            }else{
                s = Integer.toString(licznik);
            }
            s = s+"/"+rok+" - "+jTable1.getModel().getValueAt(row, 4).toString();
            jTextField3.setText(s);
            Opis = jTable1.getModel().getValueAt(row, 3).toString();
            Plik = jTable1.getModel().getValueAt(row, 4).toString();

        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Nie wybrałeś szkolenia... \nSkąd mam wiedzieć jaki ma być\nkod zaświadczenia?\n\nZaznacz szkolenie \ni wygeneruj kod ponownie!\n\n",
                                       "Uwaga !!!",JOptionPane.WARNING_MESSAGE);
        }        
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        SimpleDateFormat format = new SimpleDateFormat( "dd.MM.yyyy" );
        String str3 = format.format(jXDatePicker1.getDate());
        
        String kod = jTextField3.getText();
        int i = kod.length();
        
        MAIN.PamiecData = str3;
        
        if (i>0){
            
            if (addD==true){
                int row = jTable1.getSelectedRow();           
                try {
                    MYSQL_BAZA.insertSzkolenie(jTextField5.getText(), str3, jTable1.getModel().getValueAt(row, 3).toString(), kod, jTable1.getModel().getValueAt(row, 4).toString());
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(DOknoSzkolenie.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                try {
                    MYSQL_BAZA.updateSzkolenie(ID, jTextField5.getText(), str3, Opis, kod, Plik);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(DOknoSzkolenie.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            
            this.dispose();
            try {
                FOknoGlowne.PrzeladujSzkolenia(jTextField5.getText());
            } catch (FileNotFoundException ex) {
                Logger.getLogger(DOknoSzkolenie.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            JOptionPane.showMessageDialog(this,"Nie tak szybko !!! \nNie stworzyłeś kodu zaświadczenia\nNajpierw wybierz przycisk GENERUJ SYMBOL!\n\n",
                                       "Uwaga !!!",JOptionPane.WARNING_MESSAGE);
        }
 
    }//GEN-LAST:event_jButton2ActionPerformed

