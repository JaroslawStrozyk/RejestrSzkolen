    public void ustawDane(Boolean add, String idpracownik, String nazwisko, String imie, String idzaswiadczenie, String dataszkolenia, String rodzajszkolenia, String zaswiadczenie, String plik){
            addD = add;
            String data = "";
            
            SimpleDateFormat format = new SimpleDateFormat( "dd.MM.yyyy" );
            jXDatePicker1.setFormats(format);
            
            this.ID = idzaswiadczenie;
            this.Plik = plik;
            this.Opis = rodzajszkolenia;
            jTextField5.setText(idpracownik);
            jTextField1.setText(imie);
            jTextField4.setText(nazwisko);        

            if (addD==true){
                this.setTitle("Dodawanie nowego zaświadczenia...");
                
                try{  
                    if (MAIN.PamiecData.equals("")){
                        data = FOknoGlowne.podajDate();
                    }else{
                        data = MAIN.PamiecData;
                    };
                    jXDatePicker1.setDate(format.parse(data));
                } catch(Exception e){
                    System.out.println(e);
                }
                jTextField3.setText(null);
               
            }else{
                this.setTitle("Edytowanie wybranego zaświadczenia...");
                
                try{
                    jXDatePicker1.setDate(format.parse(dataszkolenia));
                } catch(Exception e){
                    System.out.println(e);
                }
                
                jTextField3.setText(zaswiadczenie);
            }
    }
    
    
    
    
    
    
